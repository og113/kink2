/*----------------------------------------------------------------------------------------------------------------------------
	wrapper
		program to provide a simple mpi wrapper for main
----------------------------------------------------------------------------------------------------------------------------*/

/*----------------------------------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------------------------------
	CONTENTS
		1 - loading options, argv inputs
		2 - running programs
----------------------------------------------------------------------------------------------------------------------------
----------------------------------------------------------------------------------------------------------------------------*/

#include <iostream>
#include <mpi.h>

using namespace std;

int main(int argc, char** argv) {

int id, p;

MPI_Init(&argc,&argv);

id = MPI::COMM_WORLD.Get_rank ( );
p = MPI::COMM_WORLD.Get_size ( );

if (id==0) {
	cout << p << " available processes." << endl;
}

MPI_Finalize();

return 0;
}
