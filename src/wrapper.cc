#include <iostream>
#include <mpi.h>

using namespace std;

int main(int argc, char** argv) {

MPI_Init(&argc,&argv);
int p = MPI::COMM_WORLD.Get_size ( );
cout << p << " available processes." << endl;
MPI_Finalize();

return 0;
}
